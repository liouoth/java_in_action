import java.util.concurrent.Semaphore;
import java.util.concurrent.atomic.AtomicIntegerArray;

@Slf4j
public class SemaphoreThreadPool {
    //连接池大小
    private static final int POOL_SIZE = 2;
    //连接池数组
    private static final Connection connections[];

    //信号量与连接池大小相同
    private static final Semaphore semaphore;

    AtomicIntegerArray states = new AtomicIntegerArray(new int[POOL_SIZE]);

    static {
        connections = new CustomerConnection[POOL_SIZE];
        semaphore = new Semaphore(POOL_SIZE);
    }

    public Connection borrow() {
        //获取许可
        try {
            semaphore.acquire();
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
        for (int i = 0; i < POOL_SIZE; i++) {
            if (states.compareAndSet(i, 0, 1)) {
                log.debug("返回连接：" + i);
                return connections[i];
            }
        }
        return null;
    }

    public void free(Connection connection){
        for (int i=0;i<POOL_SIZE;i++){
            if (connection==connections[i]){
                log.debug("线程池收到归还连接："+i);
                states.set(i,0);
                semaphore.release();
            }
        }
    }

    public static void main(String[] args) {
        com.leo.changeable_class.customer_threadpool.Pool pool = new com.leo.changeable_class.customer_threadpool.Pool();
        for (int i=0;i<10;i++){
            new Thread(()->{
                Connection connection = pool.borrow();
                log.debug("获取到连接");
                try {
                    Thread.sleep(2000);
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
                log.debug("归还连接");
                pool.free(connection);
            },"线程"+i).start();
        }
    }
}
